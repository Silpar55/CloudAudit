/**
 * AWS IAM Policy Generator
 *
 * Purpose: Generate IAM policies and trust policies for customer accounts
 * Responsibilities:
 * - Generate trust policies (who can assume the role)
 * - Generate permission policies (what the role can do)
 * - Return formatted JSON strings for UI display and Bash script for CloudShell
 */

import {
  getPlatformRoleArn,
  getPlatformRoleArnsForCustomerTrust,
} from "./platform-role.js";

export const generateScripts = (pendingAccount) => {
  const platformRoleArn = getPlatformRoleArn();
  const trustPrincipals = getPlatformRoleArnsForCustomerTrust();
  const principalAws =
    trustPrincipals.length === 1 ? trustPrincipals[0] : trustPrincipals;

  const trustPolicy = {
    Version: "2012-10-17",
    Statement: [
      {
        Effect: "Allow",
        Principal: {
          AWS: principalAws,
        },
        Action: "sts:AssumeRole",
        Condition: {
          StringEquals: {
            "sts:ExternalId": pendingAccount.external_id,
          },
        },
      },
    ],
  };

  const permissionsPolicy = {
    Version: "2012-10-17",
    Statement: [
      {
        Sid: "CostExplorerAndBasicAccess",
        Effect: "Allow",
        Action: [
          "sts:GetCallerIdentity",
          "ec2:DescribeInstances",
          "s3:ListAllMyBuckets",
          "ce:GetCostAndUsage",
          "ce:GetDimensionValues",
          "ce:GetCostForecast",
          "ce:GetUsageForecast",
          "ce:GetTags",
        ],
        Resource: "*",
      },
      {
        Sid: "AthenaCURQueryAccess",
        Effect: "Allow",
        Action: [
          "athena:StartQueryExecution",
          "athena:GetQueryExecution",
          "athena:GetQueryResults",
          "athena:GetWorkGroup",
          "glue:GetDatabase",
          "glue:GetTable",
          "glue:GetPartitions",
          "glue:CreateDatabase",
          "glue:CreateTable",
          "glue:UpdateTable",
          "glue:DeleteTable",
          "s3:GetBucketLocation",
          "s3:GetObject",
          "s3:ListBucket",
          "s3:PutObject",
        ],
        Resource: "*",
      },
      {
        Sid: "CURAutomatedSetupAccess",
        Effect: "Allow",
        Action: [
          "s3:CreateBucket",
          "s3:PutBucketPolicy",
          "s3:GetBucketPolicy",
          "s3:GetBucketAcl",
          "cur:PutReportDefinition",
          "cur:DescribeReportDefinitions",
        ],
        Resource: [
          "arn:aws:s3:::cloudaudit-cur-data-*",
          "arn:aws:s3:::cloudaudit-cur-data-*/*",
          "*",
        ],
      },
      {
        Sid: "CloudAuditRemediationAndMetricsAccess",
        Effect: "Allow",
        Action: [
          "cloudwatch:GetMetricStatistics",
          "cloudwatch:GetMetricData",
          "cloudwatch:ListMetrics",
          "ec2:StopInstances",
          "ec2:StartInstances",
          "rds:ModifyDBInstance",
          "rds:DescribeDBInstances",
        ],
        Resource: "*",
      },
    ],
  };

  const roleName = "CloudAuditRole";
  const policyName = "CloudAuditAccessPolicy";

  // Dynamically inject the exact JSON into the bash script
  const cloudShellScript = `#!/usr/bin/env bash
# ==============================================================================
# CloudAudit IAM Role Setup Script
# Run this in your AWS CloudShell to grant CloudAudit access to your billing data
#
# Notes:
# - Safe to re-run (idempotent): updates role trust + policy versions as needed.
# - ExternalId is unique per CloudAudit connection (security boundary).
# - If a multi-principal trust policy includes an invalid principal (e.g. a platform
#   dev role that doesn't exist), the script automatically falls back to prod-only.
# ==============================================================================

set -euo pipefail

export AWS_PAGER=""
export AWS_CLI_AUTO_PROMPT=off

ROLE_NAME="${roleName}"
POLICY_NAME="${policyName}"

ACCOUNT_ID="$(aws sts get-caller-identity --query Account --output text)"

# ExternalId generated by CloudAudit (per-connected account)
EXTERNAL_ID="${pendingAccount.external_id}"

PLATFORM_ROLE_ARN_PRIMARY="${trustPrincipals[0]}"
${trustPrincipals[1] ? `PLATFORM_ROLE_ARN_SECONDARY="${trustPrincipals[1]}"` : `PLATFORM_ROLE_ARN_SECONDARY=""`}

echo "Creating Trust Policy..."
cat << 'EOF' > trust-policy.json
${JSON.stringify(trustPolicy, null, 2)}
EOF

echo "Creating Access Policy..."
cat << 'EOF' > access-policy.json
${JSON.stringify(permissionsPolicy, null, 2)}
EOF

create_or_update_role() {
  local trust_file="$1"
  if aws iam get-role --role-name "$ROLE_NAME" >/dev/null 2>&1; then
    aws iam update-assume-role-policy \
      --role-name "$ROLE_NAME" \
      --policy-document "file://$trust_file" >/dev/null
  else
    aws iam create-role \
      --role-name "$ROLE_NAME" \
      --assume-role-policy-document "file://$trust_file" >/dev/null
  fi
}

upsert_policy_and_attach() {
  local policy_arn="arn:aws:iam::$ACCOUNT_ID:policy/$POLICY_NAME"
  if aws iam get-policy --policy-arn "$policy_arn" >/dev/null 2>&1; then
    # Create a new default version (IAM allows max 5 policy versions).
    aws iam create-policy-version \
      --policy-arn "$policy_arn" \
      --policy-document file://access-policy.json \
      --set-as-default >/dev/null

    # Prune old non-default versions so re-runs never hit the 5-version limit.
    mapfile -t versions < <(aws iam list-policy-versions --policy-arn "$policy_arn" \
      --query "Versions[?IsDefaultVersion==\`false\`].[VersionId,CreateDate]" --output text | sort -k2)
    # Keep the 3 newest non-default versions; delete older ones.
    if ((\${#versions[@]} > 3)); then
      for ((i=0; i<\${#versions[@]}-3; i++)); do
        vid="$(echo "\${versions[$i]}" | awk '{print $1}')"
        aws iam delete-policy-version --policy-arn "$policy_arn" --version-id "$vid" >/dev/null || true
      done
    fi
  else
    policy_arn="$(aws iam create-policy \
      --policy-name "$POLICY_NAME" \
      --policy-document file://access-policy.json \
      --query 'Policy.Arn' --output text)"
  fi

  aws iam attach-role-policy --role-name "$ROLE_NAME" --policy-arn "$policy_arn" >/dev/null 2>&1 || true
}

echo "Creating/Updating IAM Role ($ROLE_NAME)..."
set +e
create_or_update_role "trust-policy.json"
rc=$?
set -e

if [[ $rc -ne 0 ]]; then
  # If the trust policy contains an invalid principal, fall back to prod-only trust.
  # This commonly happens when a platform dev role ARN is referenced but doesn't exist.
  echo "WARN: Role creation/update failed (possible invalid principal). Falling back to prod-only trust..." >&2
  cat > trust-policy-prod-only.json <<EOF
{
  "Version": "2012-10-17",
  "Statement": [
    {
      "Effect": "Allow",
      "Principal": { "AWS": "${trustPrincipals[0]}" },
      "Action": "sts:AssumeRole",
      "Condition": { "StringEquals": { "sts:ExternalId": "${pendingAccount.external_id}" } }
    }
  ]
}
EOF
  create_or_update_role "trust-policy-prod-only.json"
fi

echo "Creating/Updating IAM Policy ($POLICY_NAME) and attaching to role..."
upsert_policy_and_attach

# Clean up
rm -f trust-policy.json access-policy.json trust-policy-prod-only.json

# Output the final ARN required for the CloudAudit dashboard
echo ""
echo "✅ Success! Please copy the following ARN into your CloudAudit dashboard:"
echo "arn:aws:iam::$ACCOUNT_ID:role/$ROLE_NAME"
echo ""
`;

  return {
    platformRoleArn,
    platformRoleArns: trustPrincipals,
    trustPolicyJson: JSON.stringify(trustPolicy, null, 2),
    permissionsPolicyJson: JSON.stringify(permissionsPolicy, null, 2),
    cloudShellScript,

    instructions: {
      step1: "Go to the AWS IAM Console > Roles.",
      step2:
        "Click 'Create Role', select 'Custom trust policy', and paste the Trust Policy JSON.",
      step3:
        "Click 'Next', add a new inline policy, and paste the Permissions Policy JSON.",
      externalId: pendingAccount.external_id,
    },
    cloudShellInstructions: {
      step1:
        "Log into your AWS Console and click the 'CloudShell' icon (the terminal prompt symbol `>_` at the top right).",
      step2: "Wait a few seconds for the terminal environment to prepare.",
      step3:
        "Paste the entire script below into the terminal and press Enter. It will automatically output your new Role ARN!",
    },
  };
};

